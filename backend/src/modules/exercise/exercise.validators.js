import { z } from 'zod';

export const createExerciseSchema = {
  body: z.object({
    name: z.string().min(2),
    slug: z.string().min(2), // usually auto-generated by frontend or service, but required here
    description: z.string().optional(),
    muscleGroup: z.enum(['chest', 'back', 'shoulders', 'biceps', 'triceps', 'legs', 'core', 'cardio', 'fullBody', 'custom']),
    equipment: z.string().optional(),
    difficulty: z.enum(['beginner', 'intermediate', 'advanced']).optional(),
    instructions: z.array(z.string()).optional(),
    thumbnail: z.string().url().optional(),
    videoUrl: z.string().url().optional(),
  }),
};

export const updateExerciseSchema = {
  body: z.object({
    name: z.string().min(2).optional(),
    slug: z.string().min(2).optional(),
    description: z.string().optional(),
    muscleGroup: z.enum(['chest', 'back', 'shoulders', 'biceps', 'triceps', 'legs', 'core', 'cardio', 'fullBody', 'custom']).optional(),
    equipment: z.string().optional(),
    difficulty: z.enum(['beginner', 'intermediate', 'advanced']).optional(),
    instructions: z.array(z.string()).optional(),
    thumbnail: z.string().url().optional(),
    videoUrl: z.string().url().optional(),
    active: z.boolean().optional(),
  }),
};
